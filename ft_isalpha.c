#include "libft.h"

int	ft_isalpha(int c)
{
	while (c != '\0')
	{
		if (c >= 'a' && c <= 'z')
			return (c);
		if (c >= 'A' && c <= 'Z')
			return (c);
		else
			return (0);
	}
	return (c);
}
